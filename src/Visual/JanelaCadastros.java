    public void atualizaTabela() {
        listaObjetos.clear();
        listaObjetos.addAll(dao.getLista());
        int linha = listaObjetos.size() - 1;
        if (linha >= 0) {
            tabelaObjetos.setRowSelectionInterval(linha, linha);
            tabelaObjetos.scrollRectToVisible(tabelaObjetos.getCellRect(linha, linha, true));
        }
    }

