    public boolean validaCampos() {
        if (!(txtRazaoSocial.getText().length() > 0
                && txtNomePosto.getText().length() > 0
                && txtCNPJ.getText().length() > 0
                && txtBandeira.getText().length() > 0
                && txtRua.getText().length() > 0
                && txtBairro.getText().length() > 0
                && txtCEP.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos do cadastro");
            return false;
        }
        return true;
    }

